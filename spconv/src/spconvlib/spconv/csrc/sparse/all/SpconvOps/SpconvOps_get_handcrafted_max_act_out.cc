int SpconvOps::get_handcrafted_max_act_out(size_t num_act_in, std::vector<int> ksize, std::vector<int> stride, std::vector<int> padding, std::vector<int> dilation)   {
  
  int res = num_act_in;
  for (int i = 0; i < ksize.size(); ++i){
      if (ksize[i] <= stride[i]){
          res *= 1;
      }
      else if (ksize[i] > stride[i]){
          res *= tv::div_up(ksize[i], stride[i]);
      }
      else{
          res *= ksize[i];
      }
  }
  return res;
}
